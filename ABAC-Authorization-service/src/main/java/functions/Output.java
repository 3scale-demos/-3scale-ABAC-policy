package functions;

public class Output {
    private boolean isAllowed;

    public Output() {}

    public Output(boolean isAllowed) {
        this.isAllowed= isAllowed;
    }

    public boolean isIsAllowed() {
        return this.isAllowed;
    }

    public boolean getIsAllowed() {
        return this.isAllowed;
    }

    public void setIsAllowed(boolean isAllowed) {
        this.isAllowed = isAllowed;
    }
   

}
